							<p text align="justify">
								Este proyecto consiste en la sustitución del ventilador de una fuente de alimentación que ya ha dejado de hacer su función, dicha fuente funciona perfectamente, el único problema es que apaga el pc por sobrecalentamiento a modo de seguridad ya que su ventilador está parado por completo y no mantiene temperaturas optimas en la fuente de alimentación.
								</br>
								</br>
								Para ello procedemos como siempre y nos colocamos en un área despejada. Lo primero que debemos hacer es desconectar el PC de la toma eléctrica y una vez hecho pulsar un par de veces el botón de encendido del ordenador para asegurarnos que toda la electricidad acumulada en la fuente se descarga. Una vez desconectado todo procedemos a descargarnos de electricidad estática (tocando cualquier objeto metálico) si no disponemos de una pulsera para ello. Después desconectamos todos los elementos a los que la fuente suministra energía y la sacamos.
								</br>
								</br>
								A continuación, comenzamos desatornillando la fuente para poder abrirla y realizar la sustitución, asegurándonos de que quitamos los tornillos que ensamblan la caja y no los que sujetan el ventilador a la fuente ya que esos por ahora es mejor dejarlos para trabajar más cómodos. Una vez abierta separamos las piezas con cuidado ya que el ventilador viene unido mediante un sistema de 3 pines a la placa de la fuente, desconectamos el ventilador y ya podremos abrirla del todo.
								</br>
								</br>
								Desatornillamos el ventilador y ponemos el nuevo. En mi caso opte por utilizar el ventilador que arreglamos en el proyecto anterior ya que funciona perfectamente y además viene acompañado de luces led azules que dan a la fuente un toque mas moderno. Si el ventilador se puede conectar a la placa solo tendríamos que conectarlo y cerrar la fuente, en mi caso al tener conectores diferentes tuve que conectarlo a la placa, por lo que solamente fue necesario juntar el cable al resto de cables de componentes y conectarlo una vez instalado de nuevo en el chasis.
								</br>
								</br>
								Si lo deseamos antes de atornillar todo podemos conectar los cables de alimentación principales a la placa base y encender el pc una vez conectado a la corriente para asegurarnos que todo funciona antes de cerrar. Con el OK de que todo esta correcto atornillamos la fuente de nuevo y conectamos todo dejando la fuente como nueva y con un aspecto algo más juvenil.								
