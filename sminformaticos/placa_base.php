							<p text align="justify">
							Este proyecto consiste en la limpieza de los componentes internos de un ordenador, para asegurar una correcta refrigeración y un mantenimiento del equipo. Comenzaremos descargando la electricidad estática de nuestro cuerpo sujetando un objeto metálico unos segundos o mediante una pulsera antiestática si disponemos de ella.
							</br>
							</br>
							Primero nos aseguraremos de que todos los periféricos y el cable de alimentación de la torre estén desconectados. Una vez hecho esto comenzamos desatornillando y destapando la tapa superior del equipo (ya que en este caso es un gabinete de oficina), quitada la tapa procedemos a extraer el lector DVD-ROM para trabajar mejor. Desconectamos todos los cables internos de alimentación de la fuente (cable ATX 24 pines, ATX 12v 4-8 pines, cables SATA, molex, etc.) la desatornillamos y la retiramos con cuidado de no dañar ningún otro componente.
							</br>
							</br>
							Una vez quitada, seguimos con la extracción del resto de elementos, como son el disipador del procesador, la memoria RAM, discos duros y por ultimo la placa base, desatornillando en diagonal cada tornillo para asegurarnos de que va saliendo poco a poco. Con el gabinete vacío podemos limpiarlo con una brocha o con una botella de aire comprimido para asegurarnos de que queda completamente limpio.
							</br>
							</br>
							El montaje de piezas lo haremos igual, siguiendo el mismo procedimiento y orden, atornillar la placa poco a poco en cada tornillo para asegurar un agarre perfecto, conectar bien todos los cables de la fuente, encajar las RAM en su socket correspondiente, etc. Si es necesario antes de montar el disipador aplicaremos pasta térmica (como el tamaño de una lenteja) retirando la anterior con un trapo de algodón y un poco de alcohol isopropílico. Una vez esté nos aseguremos de que todo esta perfectamente colocado volvemos a poner la tapa superior y atornillamos para completar el trabajo.
							</p>
