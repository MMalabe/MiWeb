								</br>
								</br>
								A continuación, se inicia el proceso de instalación tras un pequeño periodo de carga. Lo primero que debemos hacer es seleccionar el idioma del sistema y el de la entrada de texto (teclado), lo dejamos por defecto ya que viene en español, salvo que deseemos otro idioma. Después introducimos el serial del que disponemos para que permitirá continuar con la instalación y elegimos la versión de Windows 8 más acorde a nuestras necesidades. Elegimos instalación personalizada para hacer una instalación limpia, cuando estemos en la ventana que indica las particiones (o discos duros) que tiene el ordenador las seleccionamos y le damos a formatear borrando todo y comenzando el proceso de instalación en la partición que deseemos.
								</br>
								</br>
								Una vez finalizada la instalación ya podremos retirar el USB con la imagen y configurar el equipo a nuestro gusto con temas, fondo de pantalla, salvapantallas y por supuesto los últimos controladores, las Office (Microsoft u Open Office), navegador, antivirus, anti-malwares, CCleaner para mantener el equipo limpio, etc.
