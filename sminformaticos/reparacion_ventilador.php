							<p text align="justify">
								Este proyecto consiste en la reparación de un viejo ventilador para poder reutilizarlo y darle vida de cara a algún futuro proyecto. Es un ventilador estándar de 14mm de marca Zalman con luces led que, debido al uso, paso del tiempo y acumulación de polvo no refrigera como debe, ya que sus hélices no giran correctamente debido a la falta de lubricación en el eje y suciedad.
								</br>
								</br>
								Para ello nos situamos en un lugar de trabajo donde nada nos estorbe y dispongamos de una buena iluminación, además de prepararnos con unas pinzas (serán necesarias mas adelante), un pequeño trapo para limpiar las hélices correctamente, una brocha para rincones donde no podamos llegar y lo más importante, aceite lubricante industrial, ya sea en gotas o spray.
								</br>
								</br>
								Primero examinamos bien nuestro ventilador para asegurarnos de que se puede desmontar perfectamente, ya que algunos pueden dar problemas a la hora de ello o son mas complejos. Por lo general traen una pequeña arandela de plástico que sujeta las hélices del ventilador al eje del motor que debemos localizar, suele estar debajo de una pegatina donde viene la marca y datos del fabricante. Levantamos dicha pegatina y con ayuda de las pinzas antes mencionadas sacamos la arandela con cuidado de no romperla. Una vez extraída podremos separar las hélices del eje.
								</br>
								</br>
								Una vez separadas limpiamos las hélices con el trapo para quitar todo el polvo y hacemos lo mismo con el eje donde van situadas. Después aplicamos un poco de aceite, un par de gotas serán suficientes, o unas pulverizadas si es en spray al eje y al sistema de rotación donde dicho eje va situado. A continuación, unimos ambas piezas ya lubricadas y las hacemos girar antes de colocar nada mas para que la lubricación vaya surgiendo efecto o ver si es necesario aplicar algo más de aceite.
								</br>
								</br>
								De no ser necesario aplicar nada mas nos aseguramos que encajan bien las piezas y volvemos a colocar la arandela que junta las hélices del ventilador presionando fuerte hasta escuchar un click, después sellamos de nuevo por completo la zona con la protección extra, así evitaremos que se derrame algo de aceite que pueda quedar restante, limpiamos y tapamos con la pegatina de nuevo. Y con esto tendremos el ventilador girando de nuevo a su máxima velocidad y sin apenas ruido.
							</p>
